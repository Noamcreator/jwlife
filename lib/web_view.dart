<div id="section1" class="section">
<div class="pGroup">
<p id="p3" data-pid="3" class="p3 sb disableActiveState"><span class="parNum refID" data-pnum="1">&nbsp;</span>En cette période d’incertitudes, nous ne pouvons pas maîtriser tout ce qui nous arrive. Mais en général, notre bonheur dépend davantage de notre état d’esprit que de notre situation. La Bible dit que, malgré leurs problèmes, «&nbsp;les gens heureux vivent dans une fête continuelle&nbsp;» (<a class="jsBibleLink jsHasModalListener jsNoScroll" data-bible="nwtsty" data-requested-bible="" data-targetverses="20015015" href="/fr/biblioth%C3%A8que/bible/bible-d-etude/livres/proverbes/15/#v20015015" target="_blank">Proverbes 15:15</a>, <em>Bible Nouvelle Français courant</em>). Que pouvons-nous faire pour être plus heureux&nbsp;? Voyons quelques conseils de la Bible à ce sujet.</p>
</div>
</div>
<div id="section2" class="section">
<h2 id="p4" data-pid="4" class="disableActiveState p4">Luttez contre l’inquiétude</h2>
<div class="pGroup">
<p id="p5" data-pid="5" class="p5 sb disableActiveState"><span class="parNum refID" data-pnum="2">&nbsp;</span>La Bible dit&nbsp;: «&nbsp;L’inquiétude dans le cœur d’un homme l’oppresse, alors qu’une bonne parole le réjouit&nbsp;» (<a class="jsBibleLink jsHasModalListener jsNoScroll" data-bible="nwtsty" data-requested-bible="" data-targetverses="20012025" href="/fr/biblioth%C3%A8que/bible/bible-d-etude/livres/proverbes/12/#v20012025" target="_blank">Proverbes 12:25</a>).</p>
<ul><li><p id="p6" data-pid="6" class="p6 su disableActiveState">      <span class="parNum refID" data-pnum="3">&nbsp;</span>       La Bible peut vous aider à faire face à une inquiétude excessive. Pour en savoir plus, lisez l’article «&nbsp;<a class="pub-g16" data-page-id="mid102016047" href="/fr/biblioth%C3%A8que/revues/reveillez-vous-no2-2016-avril/inquietude-d-apres-la-bible/">Ce que la Bible dit au sujet de l’inquiétude</a>&nbsp;».</p>
</li>
</ul>
</div>
</div>
<div id="section3" class="section">
<h2 id="p7" data-pid="7" class="disableActiveState p7">Combattez la solitude</h2>
<div class="pGroup">
<p id="p8" data-pid="8" class="p8 sb disableActiveState"><span class="parNum refID" data-pnum="4">&nbsp;</span>La Bible dit&nbsp;: «&nbsp;Un véritable ami témoigne son amour en tout temps, et c’est un frère qui est né pour les moments de détresse&nbsp;» (<a class="jsBibleLink jsHasModalListener jsNoScroll" data-bible="nwtsty" data-requested-bible="" data-targetverses="20017017" href="/fr/biblioth%C3%A8que/bible/bible-d-etude/livres/proverbes/17/#v20017017" target="_blank">Proverbes 17:17</a>).</p>
<ul><li><p id="p9" data-pid="9" class="p9 su disableActiveState">       <span class="parNum refID" data-pnum="5">&nbsp;</span>       La Bible donne des conseils pour consolider ses amitiés et ainsi lutter contre la solitude. Lisez l’article «&nbsp;<a class="pub-mrt" data-page-id="mid501100098" href="/fr/biblioth%C3%A8que/rubriques/divers/combattre-solitude-bible-aide/">Combattre la solitude en nouant des amitiés&nbsp;: quelle aide la Bible peut-elle apporter&nbsp;?</a>&nbsp;».</p>
</li>
</ul>
</div>
</div>
<div id="section4" class="section">
<h2 id="p10" data-pid="10" class="disableActiveState p10">Développez votre amour pour Dieu et pour les gens</h2>
<div class="pGroup">
<p id="p11" data-pid="11" class="p11 sb disableActiveState"><span class="parNum refID" data-pnum="6">&nbsp;</span>La Bible dit&nbsp;: «&nbsp;Tu dois aimer Jéhovah<span id="footnotesource1" class="refID">&nbsp;</span><a href="/fr/biblioth%C3%A8que/rubriques/divers/rester-heureux-periode-d-incertitudes/#footnote1" data-anchor="#footnote1" class="footnoteLink jsHasModalListener jsNoScroll">a</a> ton Dieu de tout ton cœur, de toute ton âme et de toute ta pensée. […] Tu dois aimer ton prochain comme toi-même&nbsp;» (<a class="jsBibleLink jsHasModalListener jsNoScroll" data-bible="nwtsty" data-requested-bible="" data-targetverses="40022037-40022039" href="/fr/biblioth%C3%A8que/bible/bible-d-etude/livres/matthieu/22/#v40022037-v40022039" target="_blank">Matthieu 22:37-39</a>).</p>
<ul><li><p id="p12" data-pid="12" class="p12 su disableActiveState">       <span class="parNum refID" data-pnum="7">&nbsp;</span>       La prière peut vous aider à approfondir votre amour pour Dieu. Pour en savoir plus, lisez l’article «&nbsp;<a class="pub-wp21" data-page-id="mid2021004" href="/fr/biblioth%C3%A8que/revues/tour-de-garde-no1-2021-janv-fevr/dieu-entend-il-nos-prieres/">Dieu entend-il nos prières&nbsp;?</a>&nbsp;».</p>
</li>
<li><p id="p13" data-pid="13" class="p13 su disableActiveState">       <span class="parNum refID" data-pnum="8">&nbsp;</span>       Suivre ce qu’on appelle la Règle d’or vous aidera à manifester votre amour aux gens. Vous en apprendrez davantage en lisant l’article «&nbsp;<a class="pub-ijwbq" data-page-id="mid500100108" href="/fr/la-bible-et-vous/questions-bibliques/regle-d-or/">Qu’est-ce que la Règle d’or&nbsp;?</a>&nbsp;».</p>
</li>
</ul>
<p id="p14" data-pid="14" class="p14 sb disableActiveState"><span class="parNum refID" data-pnum="9">&nbsp;</span>Aimeriez-vous connaître d’autres conseils que la Bible donne pour être heureux en cette période d’incertitudes&nbsp;? Si oui, essayez notre <a class="pub-ijw" data-page-id="mid1011722" href="/fr/la-bible-et-vous/cours-biblique-particulier/">cours biblique interactif gratuit</a>.</p>
</div>
